int	ft_str_is_printable(char *str)
{
	int	i;
	int	c;

	i = 0;
	while (str[i])
	{
		c = str[i];
		if (c <= 30 || c >= 127)
		{
			return (0);
		}
		i++;
	}
	return (1);
}
